package ClassWorks.calc2;

public class Model {
    private int a, b;

    Model(int a, int b) {
        this.a = a;
        this.b = a;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
